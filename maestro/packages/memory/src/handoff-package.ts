function renderDoNotTouch(project: Project, metadata: any, workspace: RunWorkspace | undefined): string {
  const ignoredPatterns = [
    ".git/",
    "node_modules/",
    "dist/",
    "build/",
    "out/",
    ".next/",
    ".nuxt/",
    "target/",
    "bin/",
    "obj/",
    "*.lock (unless explicitly instructed)",
    "package-lock.json (unless explicitly instructed)",
    "yarn.lock (unless explicitly instructed)",
    "pnpm-lock.yaml (unless explicitly instructed)",
    ".env",
    ".env.local",
    ".env.production",
    "Files outside the task scope"
  ];

  return `# Do Not Touch

These files and folders should **not be modified** unless explicitly instructed in the approved plan.

## Standard Exclusions

${ignoredPatterns.map(pattern => `- ${pattern}`).join("\n")}

## Project-Specific Exclusions

${metadata.snapshotIgnorePatterns && metadata.snapshotIgnorePatterns.length > 0
    ? metadata.snapshotIgnorePatterns.map((pattern: string) => `- ${pattern}`).join("\n")
    : "- None configured"}

## Repository Path

- **Repository:** ${project.repoPath}
- **Workspace sandbox:** ${workspace?.workspacePath || "not created"}

## Important

- **Work only in the workspace sandbox when it exists.**
- **Do not modify the original repository path.**
- **Do not modify files outside the task scope.**
- **Do not touch configuration files** unless the approved plan explicitly requires it.
- **Do not modify lock files** unless the approved plan explicitly requires adding or updating dependencies.
- **Do not touch .env files** or other secret/credential files.
- **Do not modify .git/** or version control metadata.

If the approved plan requires modifying any of these files, proceed carefully and document the change clearly in your report.
`;
}

