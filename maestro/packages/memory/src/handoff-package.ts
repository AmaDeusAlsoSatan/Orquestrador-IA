import { promises as fs } from "node:fs";
import path from "node:path";
import type { Project, ProjectTask, RunRecord, RunWorkspace } from "@maestro/core";
import { readRunMetadata } from "./run-prepare";

export interface HandoffPackageResult {
  handoffDir: string;
  files: string[];
}

const HANDOFF_FILES = [
  "00-read-this-first.md",
  "01-executor-rules.md",
  "02-approved-codex-plan.md",
  "03-project-context.md",
  "04-task-contract.md",
  "05-expected-report-format.md",
  "06-do-not-touch.md",
  "07-kiro-prompt.md"
];

export async function createHandoffPackage(
  run: RunRecord,
  project: Project,
  task?: ProjectTask,
  workspace?: RunWorkspace
): Promise<HandoffPackageResult> {
  const handoffDir = path.join(run.path, "handoff");
  await fs.mkdir(handoffDir, { recursive: true });

  const supervisorOutput = await readRunFile(run, "07-supervisor-output.md");
  if (!supervisorOutput) {
    throw new Error("Cannot create handoff package: supervisor output is missing. Run 'maestro run attach --stage supervisor' first.");
  }

  const contextPack = await readRunFile(run, "02-context-pack.md");
  const metadata = await readRunMetadata(run.path);

  await fs.writeFile(path.join(handoffDir, "00-read-this-first.md"), renderReadThisFirst(workspace), "utf8");
  await fs.writeFile(path.join(handoffDir, "01-executor-rules.md"), renderExecutorRules(workspace), "utf8");
  await fs.writeFile(path.join(handoffDir, "02-approved-codex-plan.md"), supervisorOutput, "utf8");
  await fs.writeFile(path.join(handoffDir, "03-project-context.md"), renderProjectContext(contextPack), "utf8");
  await fs.writeFile(path.join(handoffDir, "04-task-contract.md"), renderTaskContract(run, project, task, supervisorOutput, workspace), "utf8");
  await fs.writeFile(path.join(handoffDir, "05-expected-report-format.md"), renderExpectedReportFormat(), "utf8");
  await fs.writeFile(path.join(handoffDir, "06-do-not-touch.md"), renderDoNotTouch(project, metadata, workspace), "utf8");
  await fs.writeFile(path.join(handoffDir, "07-kiro-prompt.md"), renderKiroPrompt(run, project, task, workspace), "utf8");

  return {
    handoffDir,
    files: HANDOFF_FILES
  };
}

