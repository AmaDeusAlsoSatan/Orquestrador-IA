function renderTaskContract(
  run: RunRecord,
  project: Project,
  task: ProjectTask | undefined,
  supervisorPlan: string,
  workspace: RunWorkspace | undefined
): string {
  const acceptanceCriteria = extractAcceptanceCriteria(supervisorPlan);

  return `# Task Contract

## Run Information

${renderWorkspaceNotice(workspace)}

- **Run ID:** ${run.id}
- **Project ID:** ${project.id}
- **Project Name:** ${project.name}
- **Task ID:** ${task?.id || "none"}
- **Created At:** ${run.createdAt}
- **Workspace Sandbox:** ${workspace?.workspacePath || "not created"}
- **Original Repository:** ${project.repoPath}

## Objective

${run.goal}

${task ? `## Task Details

- **Title:** ${task.title}
- **Description:** ${task.description || "Not set"}
- **Status:** ${task.status}
- **Priority:** ${task.priority}
- **Tags:** ${task.tags.length > 0 ? task.tags.join(", ") : "none"}
` : ""}

## Acceptance Criteria

${acceptanceCriteria}

## Contract

By executing this task, you agree to:

- Follow the approved Codex Supervisor plan.
- Implement only the approved scope.
- Report all changes, tests, and risks.
- Use the required report format.
- Stop and ask if anything is unclear.
`;
}

function extractAcceptanceCriteria(supervisorPlan: string): string {
  const lines = supervisorPlan.split(/\r?\n/);
  const criteriaKeywords = [
    "acceptance criteria",
    "critérios de aceite",
    "criterios de aceite",
    "validation",
    "validação",
    "validacion",
    "checklist",
    "tests",
    "testes"
  ];

  let startIndex = -1;
  let endIndex = -1;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].toLowerCase().trim();
    if (criteriaKeywords.some(keyword => line.includes(keyword))) {
      startIndex = i;
      break;
    }
  }

  if (startIndex === -1) {
    return "_Acceptance criteria not automatically extracted. Review the approved Codex plan in 02-approved-codex-plan.md._";
  }

  for (let i = startIndex + 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (line.startsWith("##") || line.startsWith("# ")) {
      endIndex = i;
      break;
    }
  }

  if (endIndex === -1) {
    endIndex = lines.length;
  }

  const extracted = lines.slice(startIndex, endIndex).join("\n").trim();
  return extracted || "_Acceptance criteria not automatically extracted. Review the approved Codex plan in 02-approved-codex-plan.md._";
}

