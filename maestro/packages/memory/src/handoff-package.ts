function renderWorkspaceNotice(workspace: RunWorkspace | undefined): string {
  if (!workspace) {
    return `## Workspace Sandbox

Aviso: esta run ainda nao tem workspace sandbox. Recomenda-se criar antes de entregar ao Kiro:

\`\`\`bash
maestro run workspace create --run <run-id>
\`\`\`
`;
  }

  return `## Workspace Sandbox

IMPORTANTE:
Trabalhe somente neste workspace sandbox:
${workspace.workspacePath}

Nao altere o repositorio original:
${workspace.sourceRepoPath}
`;
}

async function readRunFile(run: RunRecord, fileName: string): Promise<string | undefined> {
  try {
    return await fs.readFile(path.join(run.path, fileName), "utf8");
  } catch {
    return undefined;
  }
}
